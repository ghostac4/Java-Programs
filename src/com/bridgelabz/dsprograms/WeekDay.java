package com.bridgelabz.dsprograms;

public class WeekDay
{

   private String day;
   private String date;

   /**
    * function to get days
    * @return String day
    */
   public String getDay()
   {
      return day;
   }

   public void setDay(String day)
   {
      this.day = day;
   }

   public String getDate()
   {
      return date;
   }

   public void setDate(String date)
   {
      this.date = date;
   }
}
