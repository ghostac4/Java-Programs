package com.bridgelab.dsprograms;

public class WeekDay {

	private String day;
	private String date;

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
}
